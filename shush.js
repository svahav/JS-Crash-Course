function dayOfWeek(dayNumber ){
    switch(dayNumber){
        case 1:
            console.log("Sunday");
        case 2:
            console.log("Monday"); 
        case 3:
            console.log("Tuesday"); 
        case 4:
            console.log("Wendesday");
        case 5:
            console.log("Thursday"); 
        case 6:
            console.log("Friday");  
        case 7:
            console.log("Suterday");     
    }
}

const dayNumber1=3;
dayOfWeek(dayNumber1);